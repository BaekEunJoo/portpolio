  CREATE OR REPLACE PROCEDURE "IMUSEUM"."PRC_ORDERNUM_RESET" (SEQ_NAME IN VARCHAR2)
IS
    L_VAL NUMBER;
BEGIN

    EXECUTE IMMEDIATE 'SELECT '|| SEQ_NAME ||'.NEXTVAL FROM DUAL' INTO L_VAL;

    EXECUTE IMMEDIATE 'ALTER SEQUENCE '|| SEQ_NAME ||' INCREMENT BY -'|| L_VAL ||' MINVALUE 0';

    EXECUTE IMMEDIATE 'SELECT '|| SEQ_NAME ||'.NEXTVAL FROM DUAL' INTO L_VAL;

    EXECUTE IMMEDIATE 'ALTER SEQUENCE '|| SEQ_NAME ||' INCREMENT BY 1 MINVALUE 0';

END;

DECLARE
    X NUMBER;
BEGIN
        SYS.DBMS_JOB.SUBMIT
        (
              job => X
            , what =>
                'BEGIN
                        PRC_ORDERNUM_RESET(''ORDERNUM_SEQ'');
                 END;'
            , next_date => to_date('05-31-2017 00:00:00', 'mm/dd/yyyy hh24:mi:ss')
            , interval => 'TRUNC(SYSDATE+1)'
            , no_parse => FALSE
        );
        SYS.DBMS_OUTPUT.PUT_LINE('Job Number is : '|| to_char(X));
END;

CREATE OR REPLACE FUNCTION ZBF_GET_ORDERNUM(
    p_type in varchar2
) RETURN VARCHAR2
AS
     v_returnValue VARCHAR2(180);
BEGIN

    BEGIN

         SELECT 'S' || TO_CHAR(SYSDATE, 'YYYYMMDD') || LPAD(ORDERNUM_SEQ.nextval, 4, 0)
          into v_returnValue
          from dual;

    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        v_returnValue := ' ';
      WHEN OTHERS THEN
        v_returnValue := ' ';
    END;

    RETURN v_returnValue;
END;

